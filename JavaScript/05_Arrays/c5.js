//toString()
l1 = ['NameOne', 'NameTwo', 'NameThree', 'Namefour']
console.log(l1) //['NameOne', 'NameTwo', 'NameThree', 'Namefour']
console.log(l1.toString()) //NameOne,NameTwo,NameThree,Namefour

//value()
l1 = ['NameOne', 'NameTwo', 'NameThree', 'Namefour']
console.log(l1) //['NameOne', 'NameTwo', 'NameThree', 'Namefour']
console.log(l1.valueof()) //['NameOne', 'NameTwo', 'NameThree', 'Namefour']

//fill()
l1 = ['NameOne', 'NameTwo', 'NameThree', 'Namefour']
console.log(l1) //['NameOne', 'NameTwo', 'NameThree', 'Namefour']
console.log(l1.fill()) //[10, 10, 10, 10]
